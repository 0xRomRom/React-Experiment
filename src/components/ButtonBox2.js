const ButtonBox2 = (props) => {
  const flipHandler = () => {
    props.tossed((prevState) => prevState + 1);
    const odds = Math.random();
    let outcome = "";
    if (odds >= 0.5) {
      outcome = "Heads";
      props.image(heads);
      props.onCoin(outcome);
      return;
    }
    if (odds < 0.5) {
      outcome = "Tails";
      props.image(tails);
      props.onCoin(outcome);
      return;
    }
  };

  const letterHandler = () => {
    const randomInt = Math.round(Math.random() * 25);
    props.letter(alphabet[randomInt]);
  };

  const countryHandler = () => {
    const randomInt = Math.round(Math.random() * 205);
    props.country(country_list[randomInt]);
  };

  return (
    <Card>
      <div className={classes.innerButtonBox}>
        <button className={classes.ctaBtn} onClick={flipHandler}>
          Coin Flip
        </button>
        <button className={classes.ctaBtn} onClick={letterHandler}>
          Random Letter
        </button>
        <button className={classes.ctaBtn} onClick={countryHandler}>
          Random Country
        </button>
      </div>
    </Card>
  );
};

export default ButtonBox2;
